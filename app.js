const app = express();
const port = process.env.PORT || 3011;
app.use(express.json());
app.use(cors());


app.get('/proxy', async (req, res, next) => {
  console.log(req.url, req.query);
  try {
    const queryUrl = req.query.url || "https://public.openrec.tv/external/api/v5/movies?sort=created_at&channel_ids=simoyaka,Yaritaiji,ringomoti";
    if(queryUrl != ""){
      const decodeUrl = decodeURIComponent(queryUrl);
      // const encodedString = encodeURIComponent(originalString);
      response = await axios.get(decodeUrl);
      // console.log(response.data);  
      res.json(response.data);
    }else{
      res.json([]);
    }
  
  } catch (error) {
    // next(error);
    res.json([]);
  }
});

app.get("/", (req, res) => res.type('html').send(html));

